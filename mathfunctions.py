import math

# Because I used import, I can access methods by referencing the math module
print("ceil(3.4) = ", math.ceil(3.4))
print("floor(3.4) = ", math.floor(3.4))
print("fab(-3.4) = ", math.fabs(-3.4))

# Finding the factorial
print("factorial(7) = ", math.factorial(7))

# Return reminder of division
print("fmod(5,4) = ", math.fmod(5,4))

# Receive a float and return an int
print("trunc(4.2) = ", math.trunc(4.2))

# Return the square root
print("sqrt(5) = ", math.sqrt(5))

# Special value
print("math.e = ", math.e)
print("math.pi = ", math.pi)

# Return e^x
print("exp(4) = ", math.exp(4))

# Return the natural logarithm e * e ~= 20 so log(20) tells
# you that e^3 ~= 20
print("log(20) = ", math.log(20))

# Define the base and 10^3 = 1000
print("log(100,10) = ", math.log(100, 10))

# Also base 10 can be use like this
print("log10(1000) = ", math.log10(1000))















